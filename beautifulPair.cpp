#include<bits/stdc++.h>
using namespace std;
typedef long long int ll;

int main(int argc, char const *argv[])
{
    ll n; scanf("%lld",&n);
    ll arr[n];
    ll brr[n];
    for(int i=0; i<n; i++){
        scanf("%lld",&arr[i]);
    }
    for(int i=0; i<n; i++){
        scanf("%lld",&brr[i]);
    }
    
    return 0;
}
